#include "CarTrack.h"

void CarTrack::Draw(SDL_Renderer* renderer)
{

    SDL_SetRenderDrawColor(renderer, 0, 0, 0, SDL_ALPHA_OPAQUE);
   // SDL_RenderClear(renderer);

    SDL_SetRenderDrawColor(renderer, 255, 255, 255, SDL_ALPHA_OPAQUE);
    SDL_RenderDrawLine(renderer, 0, startPos + (trackWidth/2), endPos, startPos + (trackWidth/2));
    SDL_RenderDrawLine(renderer, 0, startPos - (trackWidth/2), endPos, startPos - (trackWidth/2));
    SDL_RenderPresent(renderer);

}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
CarTrack::CarTrack(int startPosParam,int endPosParam,int trackWidthParam)
{
    startPos = startPosParam;
    endPos = endPosParam;
    trackWidth = trackWidthParam;

}
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
int CarTrack::getStartPos()
{
	return startPos;
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
int CarTrack::getEndPos()
{

	return endPos;
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
int CarTrack::getTrackWidth()
{

	return trackWidth;
}


