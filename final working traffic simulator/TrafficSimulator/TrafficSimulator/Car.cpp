void Car::spawn(int x , int y, SDL_Renderer* renderer)
{
	SDL_RenderClear(renderer);

	x_pos = x;
	y_pos = y;

	//Render red filled quad
    fillRect = { x, y, car_width, car_height };
	SDL_SetRenderDrawColor(renderer, 0xFF, 0x00, 0x00, 0xFF);

	SDL_RenderFillRect(renderer, &fillRect);

	//Update screen
	SDL_RenderPresent(renderer);
}

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void Car::move(int amount, SDL_Renderer* renderer)
{

	
	fillRect.x += amount;
	setXPos(fillRect.x);
	SDL_SetRenderDrawColor(renderer, 0xFF, 0x00, 0x00, 0xFF); //redraw the red color
	SDL_RenderFillRect(renderer, &fillRect);
	//Update screen
	SDL_RenderPresent(renderer);
	


}

